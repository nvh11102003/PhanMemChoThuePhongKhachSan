    public void addTableGH(ArrayList<ChiTietSP> list) {
        DefaultTableModel dtm = (DefaultTableModel) tblBangGioHang.getModel();
        dtm.setRowCount(0);
        int i = 1;
        for (ChiTietSP chiTietSP : list) {
            Object[] rowData = {
                i++,
                chiTietSP.getSanPham().getMaSP(),
                chiTietSP.getSanPham().getTenSp(),
                chiTietSP.getSoLuongTon(), chiTietSP.getGiaBan(),
                chiTietSP.getSoLuongTon() * chiTietSP.getGiaBan()
            };
            dtm.addRow(rowData);
        }
        txtTongTien.setText(numberFormat.format(tinhTien()));
    }

