    public void loadTableHD(List<HoaDon> hd) {
        DefaultTableModel dtm = (DefaultTableModel) tblHoaDon.getModel();
        dtm.setRowCount(0);
        int i = 1;
        for (HoaDon hoaDon : hd) {
            Object[] rowData = {
                i++,
                hoaDon.getMa(), hoaDon.getNgayTao(), hoaDon.getNhanVien(),
                hoaDon.tinhTrang()
            };
            dtm.addRow(rowData);
        }
    }

    public Date getDate() {

        long millis = System.currentTimeMillis();
        java.sql.Date date = new java.sql.Date(millis);

        return date;
    }

    public void loadTableCTSP(List<ChiTietSP> ctsp) {
        DefaultTableModel dtm = (DefaultTableModel) tblBangSanPham.getModel();
        dtm.setRowCount(0);
        int i = 1;
        for (ChiTietSP listCTSP : ctsp) {
            Object[] rowData = {
                i++,
                listCTSP.getSanPham().getMaSP(),
                listCTSP.getSanPham().getTenSp(), listCTSP.getNamBH(),
                listCTSP.getMoTa(), listCTSP.getSoLuongTon(),
                listCTSP.getGiaNhap(), listCTSP.getGiaBan()
            };
            dtm.addRow(rowData);
        }

    }

    public float tinhTien() {
        float tongTien = 0;
        for (ChiTietSP chiTietSP1 : chiTietSP) {
            tongTien += chiTietSP1.getSoLuongTon() * chiTietSP1.getGiaBan();

        }
        return tongTien;
    }

