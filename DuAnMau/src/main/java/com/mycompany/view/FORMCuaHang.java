    private void tblBangSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangSanPhamMouseClicked
        JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm vào giỏ hàng!!!");
        int row = tblBangSanPham.getSelectedRow();
        if (row == -1) {
            return;
        }
        ChiTietSP chiTietSP = new ChiTietSP();
        SanPham sanPham = new SanPham();
        sanPham.setMaSP(tblBangSanPham.getValueAt(row, 1).toString());
        sanPham.setTenSp(tblBangSanPham.getValueAt(row, 2).toString());
        chiTietSP.setSanPham(sanPham);
        String sl = JOptionPane.showInputDialog(this, "Mời nhập số lượng");
        chiTietSP.setSoLuongTon(Integer.parseInt(sl));
        chiTietSP.setGiaBan((float) tblBangSanPham.getValueAt(row, 7));
        this.chiTietSP.add(chiTietSP);
        addTableGH(this.chiTietSP);
    }//GEN-LAST:event_tblBangSanPhamMouseClicked

    private void btnTaoHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoHoaDonActionPerformed
        HoaDon hoaDon = getDataHD();
        if (hoaDon == null) {
            return;
        }
        hoaDonInterface.add(hoaDon);
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        loadTableHD(hoaDonInterface.getAll());
    }//GEN-LAST:event_btnTaoHoaDonActionPerformed

