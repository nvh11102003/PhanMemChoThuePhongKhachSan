    private void setIndexCbbSP(String ten){
        int count = -1;
        List<SanPham> listSP = sanPhamInterface.getAll();
        for (SanPham sanPham : listSP) {
            count ++;
            if(sanPham.getTenSp().equals(ten)){
                System.out.println(count);
                if(count >1) {
                    count = 0;
                }
                cbbSP.setSelectedIndex(count);
            }
        }
    }
    private void setIndexCbbNSX(String ten){
        int count = -1;
        List<NSX> listNSX = nSXInterface.getAll();
        for (NSX nsx : listNSX) {
            count ++;
            if(nsx.getTenNSX().equals(ten)){
                cbbNSX.setSelectedIndex(count);
            }
        }
    }
    private void setIndexCbbDSP(String ten){
        int count = -1;
        List<DongSP> listDSP = dongSPInterface.getAll();
        for (DongSP dongSP : listDSP) {
            count ++;
            if(dongSP.getTenDSP().equals(ten)){
                cbbDSP.setSelectedIndex(count);
            }
        }
    }
    private void setIndexCbbMS(String ten){
        int count = -1;
        List<MauSac> listMS = mauSacInterface.getAll();
        for (MauSac mauSac : listMS) {
            count ++;
            if(mauSac.getTenMauSac().equals(ten)){
                cbbMS.setSelectedIndex(count);
            }
        }
    }
    
    
    public void loadTable(List<ChiTietSP> ctsp) {
        DefaultTableModel dtm = (DefaultTableModel) tblCTSP.getModel();
        dtm.setRowCount(0);
        int i = 1;

        for (ChiTietSP chiTietSP : ctsp) {
            Object[] rowData = {
                i++,
                chiTietSP.getSanPham().getTenSp(),
                chiTietSP.getnSX().getTenNSX(), chiTietSP.getDongSP().getTenDSP(),
                chiTietSP.getMauSac().getTenMauSac(), chiTietSP.getSoLuongTon(),
                chiTietSP.getNamBH(), chiTietSP.getMoTa(), chiTietSP.getGiaNhap(), chiTietSP.getGiaBan(),
                chiTietSP.getId()
            };
            dtm.addRow(rowData);
        }
    }

    public ChiTietSP getData() {
        SanPham maSP = (SanPham) cbbSP.getSelectedItem();
        NSX maNSX = (NSX) cbbNSX.getSelectedItem();
        DongSP maDongSP = (DongSP) cbbDSP.getSelectedItem();
        MauSac maMauSac = (MauSac) cbbMS.getSelectedItem();
        String soluong = txtSoLuong.getText();
        String namBH = txtNamBH.getText();
        String moTa = txtMoTa.getText();
        String giaNhap = txtGiaNhap.getText();
        String giaBan = txtGiaBan.getText();
        ChiTietSP ctsp = new ChiTietSP(Integer.parseInt(namBH), moTa, Integer.parseInt(soluong), Float.parseFloat(giaNhap), Float.parseFloat(giaBan));


        ctsp.setSanPham(maSP);
        ctsp.setDongSP(maDongSP);
        ctsp.setMauSac(maMauSac);
        ctsp.setnSX(maNSX);

        return ctsp;
    }
