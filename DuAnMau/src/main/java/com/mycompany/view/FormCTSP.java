    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        ChiTietSP chiTietSP = getData();
        if (chiTietSP == null) {
            return;
        }
        if (cTSPInterface.add(chiTietSP)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadTable(cTSPInterface.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        ChiTietSP chiTietSP = getData();
        String id = lblIDCTSP.getText();
        chiTietSP.setId(id);
        if (cTSPInterface.update(id, chiTietSP)) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loadTable(cTSPInterface.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblCTSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCTSPMouseClicked
        int row = tblCTSP.getSelectedRow();
        if (row == -1) {
            return;
        }
        setIndexCbbSP(tblCTSP.getValueAt(row, 1).toString());
        setIndexCbbNSX(tblCTSP.getValueAt(row, 2).toString());
        setIndexCbbDSP(tblCTSP.getValueAt(row, 3).toString());
        setIndexCbbMS(tblCTSP.getValueAt(row, 4).toString());
        txtSoLuong.setText(tblCTSP.getValueAt(row, 5).toString());
        txtNamBH.setText(tblCTSP.getValueAt(row, 6).toString());
        txtMoTa.setText(tblCTSP.getValueAt(row, 7).toString());
        txtGiaNhap.setText(tblCTSP.getValueAt(row, 8).toString());
        txtGiaBan.setText(tblCTSP.getValueAt(row, 9).toString());
        lblIDCTSP.setText(tblCTSP.getValueAt(row, 10).toString());
    }//GEN-LAST:event_tblCTSPMouseClicked

